public class charDemo {
    public static void main(String[] args) {
        char ch1,ch2;
        ch1 = 88; //code for X
        ch2 = 'Y';
        System.out.print( "ch1 and ch2 : ");
        System.out.println(ch1+ " " +ch2);

        //now the incrememnt is added to both characters
        ch1++;
        ch2++;
        System.out.print("now the ch1 and ch2 are incremented : ");
        System.out.println(ch1+ " " +ch2);

    }
}
